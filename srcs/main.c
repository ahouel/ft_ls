#include "ft_ls.h"

int	main(int ac, char **av)
{
	t_env	e;
	int		i;

	ft_bzero(&e, sizeof(t_env));
	if ((i = parsing(ac, av, &e)) == ac)
		ft_ls(&e, ".");
	else
		while (i < ac)
			ft_ls(&e, av[i++]);
	return (0);
}
